static const char *pcmsa[] = { "", "DeviceGray", "", "DeviceRGB", "DeviceCMYK" };

int gx_default_get_params(gx_device * dev, gs_param_list * plist)
{
    int code;
    gs_param_string dns, pcms;
    gs_param_float_array msa, ibba, hwra, ma;
    int colors = dev->color_info.num_components;
    int depth = dev->color_info.depth;
    int GrayValues = dev->color_info.max_gray + 1;
    int HWSize[2];
    gs_param_int_array hwsa;
    gs_param_float_array hwma, mhwra;
    (dns).data = (const byte *) (dev->dname), (dns).size = strlen((const char *) (dns).data), (dns).persistent = ((bool) 1); {
	const char *cms = pcmsa[colors];
	if (*cms != 0)
	    (pcms).data = (const byte *) (cms), (pcms).size = strlen((const char *) (pcms).data), (pcms).persistent = ((bool) 1);
	else
	    pcms.data = 0;
    }
    (hwra.data = dev->HWResolution, hwra.size = 2, hwra.persistent = ((bool) 0));;
    (msa.data = dev->MediaSize, msa.size = 2, msa.persistent = ((bool) 0));;
    (ibba.data = dev->ImagingBBox, ibba.size = 4, ibba.persistent = ((bool) 0));;
    (ma.data = dev->Margins, ma.size = 2, ma.persistent = ((bool) 0));;
    HWSize[0] = dev->width;
    HWSize[1] = dev->height;
    (hwsa.data = HWSize, hwsa.size = 2, hwsa.persistent = ((bool) 0));;
    (hwma.data = dev->HWMargins, hwma.size = 4, hwma.persistent = ((bool) 0));;
    (mhwra.data = dev->MarginsHWResolution, mhwra.size = 2, mhwra.persistent = ((bool) 0));;
    if ((code = (*(plist)->procs->xmit_name) (plist, "OutputDevice", &dns)) < 0 || (code = (*(plist)->procs->xmit_float_array) (plist, "PageSize", &msa)) < 0 || (code = (pcms.data == 0 ? 0 : (*(plist)->procs->xmit_name) (plist, "ProcessColorModel", &pcms))) < 0 || (code = (*(plist)->procs->xmit_float_array) (plist, "HWResolution", &hwra)) < 0 || (code = (dev->ImagingBBox_set ? (*(plist)->procs->xmit_float_array) (plist, "ImagingBBox", &ibba) : (*(plist)->procs->xmit_null) (plist, "ImagingBBox"))) < 0 || (code = (*(plist)->procs->xmit_float_array) (plist, "Margins", &ma)) < 0 || (code = (*(plist)->procs->xmit_int_array) (plist, "HWSize", &hwsa)) < 0 || (code = (*(plist)->procs->xmit_float_array) (plist, ".HWMargins", &hwma)) < 0 || (code = (*(plist)->procs->xmit_float_array) (plist, ".MarginsHWResolution", &mhwra)) < 0 || (code = (*(plist)->procs->xmit_float_array) (plist, ".MediaSize", &msa)) < 0 || (code = (*(plist)->procs->xmit_string) (plist, "Name", &dns)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "Colors", &colors)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "BitsPerPixel", &depth)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "GrayValues", &GrayValues)) < 0 || (code = (*(plist)->procs->xmit_long) (plist, "PageCount", &dev->PageCount)) < 0 || (code = (*(plist)->procs->xmit_bool) (plist, ".IgnoreNumCopies", &dev->IgnoreNumCopies)) < 0)
	return code;
    if (colors > 1) {
	int RGBValues = dev->color_info.max_color + 1;
	long ColorValues = 1L << depth;
	if ((code = (*(plist)->procs->xmit_int) (plist, "RedValues", &RGBValues)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "GreenValues", &RGBValues)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "BlueValues", &RGBValues)) < 0 || (code = (*(plist)->procs->xmit_long) (plist, "ColorValues", &ColorValues)) < 0)
	    return code;
    }
    if ((*(plist)->procs->requested) (plist, "HWColorMap")) {
	byte palette[3 << 8];
	if (param_HWColorMap(dev, palette)) {
	    gs_param_string hwcms;
	    hwcms.data = palette, hwcms.size = colors << depth, hwcms.persistent = ((bool) 0);
	    if ((code = (*(plist)->procs->xmit_string) (plist, "HWColorMap", &hwcms)) < 0)
		return code;
	}
    }
    {
	int tab = (*((dev)->std_procs.get_alpha_bits)) (dev, go_text);
	int gab = (*((dev)->std_procs.get_alpha_bits)) (dev, go_graphics);
	if ((code = (*(plist)->procs->xmit_int) (plist, "TextAlphaBits", &tab)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "GraphicsAlphaBits", &gab)) < 0)
	    return code;
    }
    return 0;
}

static bool param_HWColorMap(gx_device * dev, byte * palette)
{
    int depth = dev->color_info.depth;
    int colors = dev->color_info.num_components;
    if (depth <= 8 && colors <= 3) {
	byte *p = palette;
	gx_color_value rgb[3];
	gx_color_index i;
	if (((dev)->std_procs.map_color_rgb) == 0)
	    ((dev)->std_procs.map_color_rgb = (gx_default_w_b_map_color_rgb));
	for (i = 0; (i >> depth) == 0; i++) {
	    int j;
	    if ((*((dev)->std_procs.map_color_rgb)) (dev, i, rgb) < 0)
		return ((bool) 0);
	    for (j = 0; j < colors; j++)
		*p++ = ((rgb[j]) >> ((sizeof(gx_color_value) * 8) - 8));
	}
	return ((bool) 1);
    }
    return ((bool) 0);
}

static int param_MediaSize(gs_param_list *, gs_param_name, const float *, gs_param_float_array *);
static int param_check_long(gs_param_list *, gs_param_name, long, bool);
static int param_check_bytes(gs_param_list *, gs_param_name, const byte *, uint, bool);
int gs_putdeviceparams(gx_device * dev, gs_param_list * plist)
{
    bool was_open = dev->is_open;
    int code;
    gx_device_set_procs(dev);
    if (((dev)->std_procs.put_params) == 0)
	((dev)->std_procs.put_params = (gx_default_put_params));
    if (((dev)->std_procs.get_alpha_bits) == 0)
	((dev)->std_procs.get_alpha_bits = (gx_default_get_alpha_bits));
    code = (*((dev)->std_procs.put_params)) (dev, plist);
    return (code < 0 ? code : was_open && !dev->is_open ? 1 : code);
}

int gx_default_put_params(gx_device * dev, gs_param_list * plist)
{
    int ecode = 0;
    int code;
    gs_param_name param_name;
    gs_param_float_array hwra;
    gs_param_int_array hwsa;
    gs_param_float_array msa;
    gs_param_float_array ma;
    gs_param_float_array hwma;
    gs_param_float_array mhwra;
    bool ignc = dev->IgnoreNumCopies;
    gs_param_float_array ibba;
    bool ibbnull = ((bool) 0);
    int colors = dev->color_info.num_components;
    int depth = dev->color_info.depth;
    int GrayValues = dev->color_info.max_gray + 1;
    int RGBValues = dev->color_info.max_color + 1;
    long ColorValues = 1L << depth;
    gs_param_string cms;
    switch (code = (*(plist)->procs->xmit_float_array) (plist, (param_name = "HWResolution"), &(hwra))) {
    case 0:
	if ((hwra).size != 2)
	    ecode = ((-15));
	else {
	    if (hwra.data[0] <= 0 || hwra.data[1] <= 0)
		ecode = ((-15));
	    else
		break;
	}
	goto hwre;
    default:
	ecode = code;
      hwre:(*(plist)->procs->signal_error) (plist, param_name, ecode);
    case 1:
	(hwra).data = 0;
    }
    switch (code = (*(plist)->procs->xmit_int_array) (plist, (param_name = "HWSize"), &(hwsa))) {
    case 0:
	if ((hwsa).size != 2)
	    ecode = ((-15));
	else {
	    if ((hwsa.data[0] <= 0 && hwsa.data[0] != dev->width) || (hwsa.data[1] <= 0 && hwsa.data[1] != dev->height))
		ecode = ((-15));
	    else if (hwsa.data[0] > ((~(-1L << ((1 << 2) * 8 - 1))) / (1L << 12)) || hwsa.data[1] > ((~(-1L << ((1 << 2) * 8 - 1))) / (1L << 12)))
		ecode = ((-13));
	    else
		break;
	}
	goto hwse;
    default:
	ecode = code;
      hwse:(*(plist)->procs->signal_error) (plist, param_name, ecode);
    case 1:
	(hwsa).data = 0;
    }
    {
	const float *res = (hwra.data == 0 ? dev->HWResolution : hwra.data);
	const float *data;
	code = param_MediaSize(plist, "PageSize", res, &msa);
	if (code < 0)
	    ecode = code;
	data = msa.data;
	code = param_MediaSize(plist, ".MediaSize", res, &msa);
	if (code < 0)
	    ecode = code;
	else if (msa.data == 0)
	    msa.data = data;
    }
    switch (code = (*(plist)->procs->xmit_float_array) (plist, (param_name = "Margins"), &(ma))) {
    case 0:
	if ((ma).size != 2)
	    ecode = ((-15));
	else {
	    break;
	}
	goto me;
    default:
	ecode = code;
      me:(*(plist)->procs->signal_error) (plist, param_name, ecode);
    case 1:
	(ma).data = 0;
    }
    switch (code = (*(plist)->procs->xmit_float_array) (plist, (param_name = ".HWMargins"), &(hwma))) {
    case 0:
	if ((hwma).size != 4)
	    ecode = ((-15));
	else {
	    break;
	}
	goto hwme;
    default:
	ecode = code;
      hwme:(*(plist)->procs->signal_error) (plist, param_name, ecode);
    case 1:
	(hwma).data = 0;
    }
    switch (code = (*(plist)->procs->xmit_float_array) (plist, (param_name = ".MarginsHWResolution"), &(mhwra))) {
    case 0:
	if ((mhwra).size != 2)
	    ecode = ((-15));
	else {
	    if (mhwra.data[0] != dev->MarginsHWResolution[0] || mhwra.data[1] != dev->MarginsHWResolution[1])
		ecode = ((-15));
	    else
		break;
	}
	goto mhwre;
    default:
	ecode = code;
      mhwre:(*(plist)->procs->signal_error) (plist, param_name, ecode);
    case 1:
	(mhwra).data = 0;
    }
    switch (code = (*(plist)->procs->xmit_bool) (plist, (param_name = ".IgnoreNumCopies"), &ignc)) {
    default:
	ecode = code;
	(*(plist)->procs->signal_error) (plist, param_name, ecode);
    case 0:
    case 1:
	break;
    }
    {
	int igni;
	switch (code = (*(plist)->procs->xmit_int) (plist, (param_name = "%MediaSource"), &igni)) {
	default:
	    ecode = code;
	    (*(plist)->procs->signal_error) (plist, param_name, ecode);
	case 0:
	case 1:
	    break;
	}
    }
    {
	int igni;
	switch (code = (*(plist)->procs->xmit_int) (plist, (param_name = "%MediaDestination"), &igni)) {
	default:
	    ecode = code;
	    (*(plist)->procs->signal_error) (plist, param_name, ecode);
	case 0:
	case 1:
	    break;
	}
    }
    switch (code = (*(plist)->procs->xmit_float_array) (plist, (param_name = "ImagingBBox"), &ibba)) {
    case 0:
	if (ibba.size != 4 || ibba.data[2] < ibba.data[0] || ibba.data[3] < ibba.data[1])
	    ecode = ((-15));
	else
	    break;
	goto ibbe;
    default:
	if ((code = (*(plist)->procs->xmit_null) (plist, param_name)) == 0) {
	    ibbnull = ((bool) 1);
	    ibba.data = 0;
	    break;
	}
	ecode = code;
      ibbe:(*(plist)->procs->signal_error) (plist, param_name, ecode);
    case 1:
	ibba.data = 0;
	break;
    }
    if ((code = param_check_bytes(plist, "OutputDevice", (const byte *) dev->dname, strlen(dev->dname), ((bool) 1))) < 0)
	ecode = code;
    if ((code = param_check_bytes(plist, "ProcessColorModel", (const byte *) pcmsa[colors], strlen(pcmsa[colors]), colors != 0)) < 0)
	ecode = code;
    if ((code = param_check_bytes(plist, "Name", (const byte *) dev->dname, strlen(dev->dname), ((bool) 1))) < 0)
	ecode = code;
    if ((code = param_check_long(plist, "Colors", (long) (colors), ((bool) 1))) < 0)
	ecode = code;
    if ((code = param_check_long(plist, "BitsPerPixel", (long) (depth), ((bool) 1))) < 0)
	ecode = code;
    if ((code = param_check_long(plist, "GrayValues", (long) (GrayValues), ((bool) 1))) < 0)
	ecode = code;
    if ((code = param_check_long(plist, "PageCount", dev->PageCount, ((bool) 1))) < 0)
	ecode = code;
    if ((code = param_check_long(plist, "RedValues", (long) (RGBValues), colors > 1)) < 0)
	ecode = code;
    if ((code = param_check_long(plist, "GreenValues", (long) (RGBValues), colors > 1)) < 0)
	ecode = code;
    if ((code = param_check_long(plist, "BlueValues", (long) (RGBValues), colors > 1)) < 0)
	ecode = code;
    if ((code = param_check_long(plist, "ColorValues", ColorValues, colors > 1)) < 0)
	ecode = code;
    if ((*(plist)->procs->xmit_string) (plist, "HWColorMap", &cms) != 1) {
	byte palette[3 << 8];
	if (param_HWColorMap(dev, palette))
	    code = param_check_bytes(plist, "HWColorMap", palette, colors << depth, ((bool) 1));
	else
	    code = param_check_bytes(plist, "HWColorMap", 0, 0, ((bool) 0));
	if (code < 0)
	    ecode = code;
    }
    if ((code = param_check_long(plist, "TextAlphaBits", (long) ((*((dev)->std_procs.get_alpha_bits)) (dev, go_text)), ((bool) 1))) < 0)
	ecode = code;
    if ((code = param_check_long(plist, "GraphicsAlphaBits", (long) ((*((dev)->std_procs.get_alpha_bits)) (dev, go_graphics)), ((bool) 1))) < 0)
	ecode = code;
    code = (*(plist)->procs->commit) (plist);
    if (ecode < 0)
	return ecode;
    if (code < 0)
	return code;
    if (hwra.data != 0 && (dev->HWResolution[0] != hwra.data[0] || dev->HWResolution[1] != hwra.data[1])) {
	if (dev->is_open)
	    gs_closedevice(dev);
	gx_device_set_resolution(dev, hwra.data[0], hwra.data[1]);
    }
    if (hwsa.data != 0 && (dev->width != hwsa.data[0] || dev->height != hwsa.data[1])) {
	if (dev->is_open)
	    gs_closedevice(dev);
	gx_device_set_width_height(dev, hwsa.data[0], hwsa.data[1]);
    }
    if (msa.data != 0 && (dev->MediaSize[0] != msa.data[0] || dev->MediaSize[1] != msa.data[1])) {
	if (dev->is_open)
	    gs_closedevice(dev);
	gx_device_set_media_size(dev, msa.data[0], msa.data[1]);
    }
    if (ma.data != 0) {
	dev->Margins[0] = ma.data[0];
	dev->Margins[1] = ma.data[1];
    }
    if (hwma.data != 0) {
	dev->HWMargins[0] = hwma.data[0];
	dev->HWMargins[1] = hwma.data[1];
	dev->HWMargins[2] = hwma.data[2];
	dev->HWMargins[3] = hwma.data[3];
    }
    dev->IgnoreNumCopies = ignc;
    if (ibba.data != 0) {
	dev->ImagingBBox[0] = ibba.data[0];
	dev->ImagingBBox[1] = ibba.data[1];
	dev->ImagingBBox[2] = ibba.data[2];
	dev->ImagingBBox[3] = ibba.data[3];
	dev->ImagingBBox_set = ((bool) 1);
    } else if (ibbnull) {
	dev->ImagingBBox_set = ((bool) 0);
    }
    return 0;
}

static int param_MediaSize(gs_param_list * plist, gs_param_name pname, const float *res, gs_param_float_array * pa)
{
    gs_param_name param_name;
    int ecode = 0;
    int code;
    switch (code = (*(plist)->procs->xmit_float_array) (plist, (param_name = pname), &(*pa))) {
    case 0:
	if ((*pa).size != 2)
	    ecode = ((-15));
	else {
	    float width_new = pa->data[0] * res[0] / 72;
	    float height_new = pa->data[1] * res[1] / 72;
	    if (width_new < 0 || height_new < 0)
		ecode = ((-15));
	    else if (width_new > ((~(-1L << ((1 << 2) * 8 - 1))) / (1L << 12)) || height_new > ((~(-1L << ((1 << 2) * 8 - 1))) / (1L << 12)))
		ecode = ((-13));
	    else
		break;
	}
	goto mse;
    default:
	ecode = code;
      mse:(*(plist)->procs->signal_error) (plist, param_name, ecode);
    case 1:
	(*pa).data = 0;
    }
    return ecode;
}

static int param_check_long(gs_param_list * plist, gs_param_name pname, long value, bool defined)
{
    int code;
    long new_value;
    switch (code = (*(plist)->procs->xmit_long) (plist, pname, &new_value)) {
    case 0:
	if (defined && new_value == value)
	    break;
	code = ((-15));
	goto e;
    default:
	if ((*(plist)->procs->xmit_null) (plist, pname) == 0)
	    return 1;
      e:(*(plist)->procs->signal_error) (plist, pname, code);
    case 1:;
    }
    return code;
}

static int param_check_bytes(gs_param_list * plist, gs_param_name pname, const byte * str, uint size, bool defined)
{
    int code;
    gs_param_string new_value;
    switch (code = (*(plist)->procs->xmit_string) (plist, pname, &new_value)) {
    case 0:
	if (defined && new_value.size == size && !memcmp((const char *) str, (const char *) new_value.data, size))
	    break;
	code = ((-15));
	goto e;
    default:
	if ((*(plist)->procs->xmit_null) (plist, pname) == 0)
	    return 1;
      e:(*(plist)->procs->signal_error) (plist, pname, code);
    case 1:;
    }
    return code;
}
