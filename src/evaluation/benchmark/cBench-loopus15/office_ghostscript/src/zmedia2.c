static void make_adjustment_matrix(const gs_point * request, const gs_rect * medium, gs_matrix * pmat, bool scale, int rotate);
static bool match_page_size(const gs_point * request, const gs_rect * medium, int policy, int orient, bool roll, float *best_mismatch, gs_matrix * pmat, gs_point * pmsize)
{
    double rx = request->x, ry = request->y;
    if (rx - medium->p.x >= -5 && rx - medium->q.x <= 5 && ry - medium->p.y >= -5 && ry - medium->q.y <= 5 && (orient < 0 || !(orient & 1))) {
	*best_mismatch = 0;
	make_adjustment_matrix(request, medium, pmat, ((bool) 0), (orient >= 0 ? orient : 0));
    } else if (rx - medium->p.y >= -5 && rx - medium->q.y <= 5 && ry - medium->p.x >= -5 && ry - medium->q.x <= 5 && (orient < 0 || (orient & 1))) {
	*best_mismatch = 0;
	make_adjustment_matrix(request, medium, pmat, ((bool) 0), (orient >= 0 ? orient : rx < ry ? -1 : 1));
    } else {
	int rotate = (orient >= 0 ? orient : rx < ry ? (medium->q.x > medium->q.y ? -1 : 0) : (medium->q.x < medium->q.y ? 1 : 0));
	bool larger = (rotate ? medium->q.y >= rx && medium->q.x >= ry : medium->q.x >= rx && medium->q.y >= ry);
	bool adjust = ((bool) 0);
	float mismatch = medium->q.x * medium->q.y - rx * ry;
	switch (policy) {
	default:
	    return ((bool) 0);
	case 3:
	    adjust = ((bool) 1);
	case 5:
	    if (fabs(mismatch) >= fabs(*best_mismatch))
		return ((bool) 0);
	    break;
	case 4:
	    adjust = ((bool) 1);
	case 6:
	    if (!larger || mismatch >= *best_mismatch)
		return ((bool) 0);
	    break;
	}
	if (adjust)
	    make_adjustment_matrix(request, medium, pmat, !larger, rotate);
	else {
	    gs_rect req_rect;
	    req_rect.p = *request;
	    req_rect.q = *request;
	    make_adjustment_matrix(request, &req_rect, pmat, ((bool) 0), rotate);
	}
	*best_mismatch = mismatch;
    }
    if (pmat->xx == 0) {
	double temp = rx;
	rx = ry, ry = temp;
    }
    pmsize->x = (rx < medium->p.x ? medium->p.x : rx > medium->q.x ? medium->q.x : rx);
    pmsize->y = (ry < medium->p.y ? medium->p.y : ry > medium->q.y ? medium->q.y : ry);
    return ((bool) 1);
}

static void make_adjustment_matrix(const gs_point * request, const gs_rect * medium, gs_matrix * pmat, bool scale, int rotate)
{
    double rx = request->x, ry = request->y;
    double mx = medium->q.x, my = medium->q.y;
    if (rotate & 1) {
	double temp = rx;
	rx = ry, ry = temp;
    }
    if (medium->p.x == 0 && mx > rx)
	mx = rx;
    if (medium->p.y == 0 && my > ry)
	my = ry;
    gs_make_translation(mx / 2, my / 2, pmat);
    if (rotate)
	gs_matrix_rotate(pmat, 90.0 * rotate, pmat);
    if (scale) {
	double xfactor = mx / rx;
	double yfactor = my / ry;
	double factor = (((xfactor) < (yfactor)) ? (xfactor) : (yfactor));
	if (factor < 1)
	    gs_matrix_scale(pmat, factor, factor, pmat);
    }
    gs_matrix_translate(pmat, -request->x / 2, -request->y / 2, pmat);
}

