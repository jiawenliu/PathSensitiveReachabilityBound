static int calendar_get_params(gx_io_device * iodev, gs_param_list * plist)
{
    int code;
    time_t t;
    struct tm *pltime;
    struct tm ltime;
    bool running;
    if (time(&t) == -1 || (pltime = localtime(&t)) == 0) {
	ltime.tm_sec = ltime.tm_min = ltime.tm_hour = ltime.tm_mday = ltime.tm_mon = ltime.tm_year = 0;
	running = ((bool) 0);
    } else {
	ltime = *pltime;
	ltime.tm_year += 1900;
	ltime.tm_mon++;
	running = ((bool) 1);
    }
    if ((code = (*(plist)->procs->xmit_int) (plist, "Year", &ltime.tm_year)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "Month", &ltime.tm_mon)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "Day", &ltime.tm_mday)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "Weekday", &ltime.tm_wday)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "Hour", &ltime.tm_hour)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "Minute", &ltime.tm_min)) < 0 || (code = (*(plist)->procs->xmit_int) (plist, "Second", &ltime.tm_sec)) < 0)
	return code;
    return (*(plist)->procs->xmit_bool) (plist, "Running", &running);
}
