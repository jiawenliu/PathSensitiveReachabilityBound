typedef unsigned char uchar;
static int patlen;
static int skip[(127 * 2 + 1) + 1];
static int skip2;
static uchar *pat;
void bmh_init(const char *pattern)
{
    int i, lastpatchar;
    pat = (uchar *) pattern;
    patlen = strlen(pattern);
    for (i = 0; i <= (127 * 2 + 1); ++i)
	skip[i] = patlen;
    for (i = 0; i < patlen; ++i)
	skip[pat[i]] = patlen - i - 1;
    lastpatchar = pat[patlen - 1];
    skip[lastpatchar] = 32767;
    skip2 = patlen;
    for (i = 0; i < patlen - 1; ++i) {
	if (pat[i] == lastpatchar)
	    skip2 = patlen - i - 1;
    }
}

char *bmh_search(const char *string, const int stringlen)
{
    int i, j;
    char *s;
    i = patlen - 1 - stringlen;
    if (i >= 0)
	return ((void *) 0);
    string += stringlen;
    for (;;) {
	while ((i += skip[((uchar *) string)[i]]) < 0);
	if (i < (32767 - stringlen))
	    return ((void *) 0);
	i -= 32767;
	j = patlen - 1;
	s = (char *) string + (i - j);
	while (--j >= 0 && s[j] == pat[j]);
	if (j < 0)
	    return s;
	if ((i += skip2) >= 0)
	    return ((void *) 0);
}}
