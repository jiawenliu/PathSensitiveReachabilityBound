void bmha_init(const char *pattern)
{
    int i, j;
    pat = (uchar *) pattern;
    patlen = strlen(pattern);
    for (i = 0; i <= (127 * 2 + 1); ++i) {
	skip[i] = patlen;
	for (j = patlen - 1; j >= 0; --j) {
	    if (lowervec[(uchar) (i)] == lowervec[(uchar) (pat[j])])
		break;
	}
	if (j >= 0)
	    skip[i] = patlen - j - 1;
	if (j == patlen - 1)
	    skip[i] = 32767;
    }
    skip2 = patlen;
    for (i = 0; i < patlen - 1; ++i) {
	if (lowervec[(uchar) (pat[i])] == lowervec[(uchar) (pat[patlen - 1])])
	    skip2 = patlen - i - 1;
    }
}

char *bmha_search(const char *string, const int stringlen)
{
    int i, j;
    char *s;
    i = patlen - 1 - stringlen;
    if (i >= 0)
	return ((void *) 0);
    string += stringlen;
    for (;;) {
	while ((i += skip[((uchar *) string)[i]]) < 0);
	if (i < (32767 - stringlen))
	    return ((void *) 0);
	i -= 32767;
	j = patlen - 1;
	s = (char *) string + (i - j);
	while (--j >= 0 && lowervec[(uchar) (s[j])] == lowervec[(uchar) (pat[j])]);
	if (j < 0)
	    return s;
	if ((i += skip2) >= 0)
	    return ((void *) 0);
}}
